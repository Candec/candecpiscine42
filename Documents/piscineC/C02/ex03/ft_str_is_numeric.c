int	ft_str_is_numeric(char *str)
{
	int flag;
	int i;

	flag = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= '0' && str[i] <= '9'))
		{
			flag = 1;
			i++;
		}
		else
			return (0);
	}
	return (flag);
}
