int	ft_sqrt(int nb)
{
	int tmp;
	int i;

	tmp = nb;
	i = 1;
	while (tmp - i > 0)
	{
		tmp = (tmp + i) / 2;
		i = nb / tmp;
	}
	if (tmp * tmp == nb)
		return (tmp);
	else
		return (0);
}
